function applyLanguage(language) {
  const dictionary = translations[language] || translations.en;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n");
    if (dictionary[key]) {
      element.textContent = dictionary[key];
    }
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const key = element.getAttribute("data-i18n-placeholder");
    if (dictionary[key]) {
      element.setAttribute("placeholder", dictionary[key]);
    }
  });

  document.documentElement.lang = language;
  localStorage.setItem("siteLanguage", language);
}

document.addEventListener("DOMContentLoaded", () => {
  const languageSelect = document.querySelector("#languageSelect");
  const savedLanguage = localStorage.getItem("siteLanguage") || "en";

  if (languageSelect) {
    languageSelect.value = savedLanguage;
    languageSelect.addEventListener("change", () => {
      applyLanguage(languageSelect.value);
    });
  }

  applyLanguage(savedLanguage);

  document.querySelectorAll(".scanner-download").forEach((link) => {
    link.addEventListener("click", async (event) => {
      if (!window.fetch || !window.URL || window.location.protocol === "file:") {
        return;
      }

      event.preventDefault();
      const response = await fetch(link.href);
      const blob = await response.blob();
      const blobUrl = URL.createObjectURL(blob);
      const temporaryLink = document.createElement("a");
      temporaryLink.href = blobUrl;
      temporaryLink.download = link.getAttribute("download") || "donation-scanner.jpeg";
      document.body.appendChild(temporaryLink);
      temporaryLink.click();
      temporaryLink.remove();
      URL.revokeObjectURL(blobUrl);
    });
  });

});
